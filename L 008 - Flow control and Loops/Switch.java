public class Switch {
    public static void main(String[] args) {
        int choice = 50;
        switch (choice){
            case '1':
                System.out.println("Goa is on");
                break;
            case '2':
                System.out.println("Pondicherry is on");
                break;
            case '3':
                System.out.println("Manali is on");
                break;
            default:
                System.out.println("Choose correctly");
        }
    }
}
